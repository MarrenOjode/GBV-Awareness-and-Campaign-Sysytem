		<div class="top-sec"><h1> 16 WAYS TO END VIOLENCE TOWARDS GIRLS.</h1> </div><hr>
          <div class="col-1">
            <div class="section">
			  <img alt="" src="images/gbv-5.jpg" style="width:700px; height:400px" /><br><br>
			  
              <p> Drawing on over 80 years of experience with some of the world's most at-risk girls, we've put together 
			  a list of some of the most effective ways to tackle violence towards girls and women.</p>
			  <p><strong>1. RAISE AWARENESS OF THE DANGERS OF HARMFUL TRADITIONS</strong><br>
				Nagah practised Female Genital Mutilation (FGM) in Egypt for 17 years until she learned first-hand the devastating impact it can have on
				girls’ lives. She is helping rural families to learn about the facts and change their attitudes to help the practice come to an end.</p>

				<p><strong>2. TACKLE VIOLENCE AGAINST GIRLS IN SCHOOL</strong><br>
				It is estimated that 246 million girls and boys are harassed and abused on their way to, and at school every year, with girls being
				particularly vulnerable. In Hanoi, Nu*, a ninth-grade student gained confidence from counselling sessions after being bullied. 
				“I now feel confident. I no longer have thoughts of giving up on school. I tell my friends if they ever face troubles, they should
				speak to the school counsellor,” she said.</p>

				<p><strong>3. CHALLENGE AND SPEAK OUT ABOUT VIOLENCE IN THE HOME</strong><br>
				Like many girls from Brazil, Charlienne, now 17, grew up in a violent household. Now she’s determined to challenge the deeply ingrained
				beliefs that lead to gender-based violence in her community.</p>
				
				<p><strong>4. TRANSFORM ATTITUDES TOWARDS HARMFUL PRACTICES AT MULTIPLE LEVELS</strong><br>
				In Guinea Bissau religious leaders, medical professionals and ex-practitioners are all coming together to bring an end to FGM, which in 
				some regions sees as much as 93% of women cut. Now, girls in the community are becoming empowered to fight back against this damaging 
				tradition.<br>

				“If I have a daughter she will not be cut. I will not allow it”, said Serem, 11</p>
				
				<p><strong>5. LISTEN TO GIRLS’ EXPERIENCES OF VIOLENCE - AND THEIR SOLUTIONS</strong><br>
				We can only tackle gender-based violence if we listen to girls' experiences and respond to their needs.<br>

				For example, in our recent report, young people from Spain, Uganda and Colombia identified the opportunity to use social networks and
				media to promote gender equality and girls’ rights to tackle the accepted behaviours that allow gender-based violence to flourish.</p>

				<p><strong>6. HELP MAKE  GIRLS’ JOURNEYS TO SCHOOL SAFER</strong><br>
				Around 10,000 adolescent girls travel by bus in Hanoi every month, risking sexual violence, harassment and theft.<br>

				We’re working with adolescent girls, bus drivers and ticket inspectors to make buses safe in Hanoi, Vietnam, so girls can travel through
				the city free from abuse.</p>

				<p><strong>7. CONNECT SPECIALISTS AND AT-RISK COMMUNITIES</strong><br>
				Poverty and sexual violence are rife in rural areas of Nicaragua. Rape cases are often ignored and convictions rarely made, while some 
				communities feel their only option is to settle their case by a traditional justice system that can victimise those attacked.<br>

				Special Police Commissioner Carmen Poveda is at the forefront of the effort to prevent sexual violence in rural areas of Nicaragua.</p>
				
				<p><strong>8. ENGAGE RESPECTED COMMUNITY ELDERS IN THE FIGHT AGAINST VIOLENCE</strong><br>
				In remote areas of Kenya, traditional leadership is often the only power structure communities recognise. Plan International is working with 
				elders from the Ameru community, who are campaigning to keep girls safe from this dangerous tradition.<br>

				“I fight FGM because it robs women of their dignity and self-esteem,” said Mzee Mwamba, a respected Njuri-Ncheke elder.</p>
				
				<p><strong>9. MOBILISE YOUTH TO FIGHT HARMFUL PRACTICES SUCH AS CHILD MARRIAGE</strong><br>
				Child marriage denies girls their right to make vital decisions about their bodies, well-being and future. It forces them out of 
				education and into a life of poor prospects, with an increased risk of violence, abuse and ill-health.<br>

				That’s why girls’ rights activists from Malawi, Bangladesh and Honduras are lobbying their governments to outlaw the practice in order to 
				put a stop child marriage, everywhere.</p>

				<p><strong>10. ENGAGE BOYS AND YOUNG MEN TO BECOME AGENTS OF CHANGE</strong><br>
				Boys in Brazil are standing up for gender equality and breaking down the attitudes that cause violence against girls and women.<br>

				“I want to make girls and women realise they don’t deserve to be beaten. Together we can fight for their rights,” said 18-year-old Taniel.</p>

				<p><strong>11. PROTECT GIRLS WHO FACE ADDITIONAL RISKS DURING EMERGENCIES</strong><br>
				Because of their gender and age, disasters and conflict increase girls' vulnerability. This puts them at increased risk of rape, abuse and 
				harmful practices such as child marriage as parents try to cope with the circumstances.<br>

				Child-protection interventions during emergencies must take into account the additional risks faced by girls and provide support that will 
				help them avoid abuse and violence.</p>

				<p><strong>12. EMBOLDEN GIRLS TO SPEAK OUT</strong><br>
				Gisele from Brazil found her voice after attending workshops about sexual exploitation, girls’ rights and advocacy. Now she shares her 
				story of rape, abuse and homelessness to encourage other girls to speak out about the violence they face.<br>

				“To all the girls and women who have suffered like I have, please, speak up! Don’t let anyone get away with it. Talk to someone you trust, 
				who believes in you… Let’s show society we don’t have to stay quiet.”</p>				 

				<p><strong>13. SHARE VITAL INFORMATION WITH THE COMMUNITY</strong><br>
				Amel, aged 9 from Egypt is trying to eradicate FGM from her community after her friend bled to death after being cut. She spreads the 
				message in her community about its harmful effects.<br>

				“We are trying to inform each other. If one of our friends is due to be circumcised we advise her against it. We invite the whole family 
				to attend awareness sessions...to understand the negative impact on girls.”</p>

				<p><strong>14. CHALLENGE RAPE CULTURE</strong><br>
				The phrase “cultura do estupro” (rape culture) came into popular use in Brazil following the sexual assault of a 16-year-old girl by up 
				to 30 men in May last year.<br>

				In response, young activists marched the streets of Codo city to demand better policies and legislation to protect girls from sexual 
				assault, with promising results.</p>

				
				<p><strong>15. REACH OUT TO MARGINALISED AND RURAL GIRLS</strong><br>
				Sixteen-year-old Brisa, an indigenous Miskito, presents a local radio show in Nicaragua that boldly tackles a range of social issues 
				including harassment, teenage pregnancy, domestic violence and human trafficking - issues which rural girls are at greater risk of. <br>

				"One thing I like the most is to interact with the listeners. When they call I know they are getting the message," said Brisa.</p>

				<p><strong>16. TAKE A STAND AGAINST REGRESSIVE FORCES</strong><br>
				1 in 3 girls have experienced violence in their lifetime and an estimated 1 in 10 have been victims of sexual violence. Plan International 
				CEO Ann-Brigitte Albrectsen recently wrote about the increasing roll-back on the hard-won rights of girls and women across the globe and why 
				we must stop it in its tracks.<br>

				“We must take a stand now, or risk further loss of rights. And when I say loss of rights this often equates to loss of life.”</p>

