			  <h2>Here are the 6 Steps to Support a Survivor</h2>
              <p> It takes courage for a survivor of gender based violence to share their story. Never underestimate your power to affect the course of a 
			  survivor’s healing journey. Here are some tools—words, actions, and resources—that can help you support someone who shares their personal 
			  experience with you.</p>

			  <p>You don’t have to be an expert—you just have to be your true self.  If someone shares their experience with you, they consider you a
			  person they look to for support, compassion, empathy and guidance. Although you can’t take away what happened to someone, you can be a 
			  source of comfort and reassurance.</p>

				<p><strong>Listen</strong><br>
				Sometimes you don’t need words (or at least not a lot of words), to be there for someone. Many people reveal that being able to tell their
				story to someone else lessens the weight of isolation, secrecy, and self-blame. Listening is in and of itself an act of love.</p>

				<p><strong>Validate</strong><br>
				Think about a time when you felt vulnerable or faced a crisis, and think of what helped you the most. Chances are it was not a specific 
				conversation you had, but it was the knowledge and comfort that the person or people you told were there for you, believed in you, were 
				on your side, and were committed to supporting you through a hard time. There are some helpful phrases you can use to show you care.<br><br>

				<i>“I believe you.”<br>
				“This is not your fault.”<br>
				“You’re not alone. I’m here for you and I’m glad you told me.”<br></i>
				<p>We are bombarded with victim-blaming myths and attitudes in our society, and they can sink in and affect the survivor. But no action excuses
				a person hurting someone else. Violence and abuse is never the survivor’s fault. That responsibility and shame lies with the perpetrator. 
				It can be helpful to communicate that gently and repeatedly.<br><br>

				<i>“Nothing you did or could’ve done differently makes this your fault.”<br>
				“The responsibility is on the person who hurt you.”<br>
				“No one ever has the right to hurt you.”<br>
				 “It may feel like you did something wrong, but you didn’t.”<br>
				“It doesn’t matter if you did or didn’t _______. No one asks to be hurt in this way.”</i></p>
				
				<p><strong>Ask what more you can do to help</strong><br>
				Violence and abuse is about power and control. It is vital for survivors to regain their sense of personal power. 
				Instead of pushing someone into taking actions for which they are not ready, ask how you can support them.</p>

				<p><strong>Know where to point someone to for more help</strong><br>
				You can best help a survivor by offering options and leaving space for them to decide where to go from there. Here are some national 
				resources—services that can point someone to local resources.</p>

				<p style="color:brown"><strong>GVRC contacts- +254719638006 or 0709 667 000<br>

				HAK 1195<br>

				Childline Kenya (for cases involving children) 116<br></strong></p>

				<p><strong>Keep an open heart</strong><br>
				Remind a survivor you are available should they like to talk about their experiences further. The healing journey can be a long one. It can be 
				full of many challenging—but sometimes joyful and liberating—conversations. It can make a big difference for a survivor to know you are there 
				to support them along the way.</p>

				<p><strong>Finally, care for yourself</strong><br>
				There is a limit to what we are able to take in and process. The stories of someone else’s hardships related to a traumatic event can impact or 
				become a part of us. This experience of second-hand trauma—often called vicarious/secondary trauma—is a human response to coming face-to-face 
				with the reality of trauma and the difficulties of the human experience.</p>

				<p>It’s important to care for yourself as you support another person. You cannot be your best self in your supportive role if you find yourself too tired to listen with care and compassion or you are overfilled with your own emotions in response to another’s trauma. These feelings are valid. Take some time after a conversation to enjoy the outdoors or do a healthy activity that makes you feel good as a way of re-centering yourself.

				Remember, you can be your best self for someone else when you give yourself the space to honor your own needs.</p>

