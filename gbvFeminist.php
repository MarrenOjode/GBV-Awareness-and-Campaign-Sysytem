		<div class="top-sec"><h2 style="color:#111"> HOW FEMINISTS SHAPED KENYA'S RESPONSE TO GBV AMID COVID-19</h2> </div><hr>
          <div class="col-1">
            <div class="section">
			  <img alt="" src="images/kenyan-feminist.jpg" style="width:700px; height:400px" /><br><br>
			  
              <p> On April 10, 2020 nation. africa published an article where Ms Wairimu Munyinyi-Wahome urged Health Cabinet Secretary Mutahi Kagwe, to 
			  integrate gender-based violence (GBV) messaging in his daily Covid-19 briefing.</p>

				<p>The executive director of Coalition on Violence Against Women-Kenya (Covaw-K) observed that Mr Kagwe had the influence to change people's 
				attitudes by reminding them that "staying at home doesn't mean you perpetuate violence against your loved one."</p>

				<p>By then, there was a horrifying global surge in domestic violence standing at an average 30 percent, prompting UN Secretary General 
				António Guterres to call for a ceasefire.</p>

				<p>Here in Kenya where GBV lifetime prevalence for women is 38 per cent and 20.9 per cent, for men, the numbers had blown up by 42 per cent.</p>

				<p>Then on April 14, Health Chief Administrative Secretary Dr Mercy Mwangangi mentioned the spike in GBV, especially sexual 
				violence perpetuated by relatives and guardians, and warned the offenders that the law "has not been suspended" and so it will catch 
				up with them. </p>
				
				<p>The following day, Mr Mutahi also reiterated the call to end the violence, calling on religious leaders to intervene and counsel people 
				against rising GBV.</p>

				<p>Ms Wahome says the Health ministry officials' response had a ripple effect as then "interventions were designed to amplify mitigation and
				response against GBV."</p>

				<p>Ms Wahome and Covaw-K is among the feminists and associated organisations that shaped Kenya's response to GBV in the mix of Covid-19, 
				a pandemic that has horrifically thrown women and girls into burning flames of physical, sexual, economic and psychological violence-most 
				perpetrated by intimate partners, relatives and guardians.</p>

				<p>On April 22, a consortium of feminist organisations petitioned the government calling for a 30 per cent allocation of Covid-19 funds 
				to GBV response.</p>
				
				<p>They were Centre for Rights, Education and Awareness (Creaw), Federation of Women Lawyers-Kenya (Fida-Kenya) and Covaw-K. Others included
				Equality Now, Groots Kenya, Sustainable Development Goals Forum Kenya and Kenya Female Advisory Organisation.</p>

				<p>The funds would be split into supporting shelters to keep safe and provide survivors with basic necessities along with medical care, 
				psychosocial and legal support. This has not happened till now but three months ago, the government listed shelters and safe houses as 
				essential services. The providers can, therefore, acquire special passes to operate during the curfew hours when the violence is most
				prevalent.</p>

				<p>The continued outcry drew the attention of President Uhuru Kenyatta, who then directed National Crime Research Centre (NCRC) to
				probe the spike.</p>

