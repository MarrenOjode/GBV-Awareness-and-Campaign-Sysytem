          <div class="col-1">
            <div class="section">
			  <img alt="" src="images/gbv-spike.jpg" style="width:700px; height:400px" /><br><br>

					<p>The National Crime Research Centre (NCRC) has established a report indicating a 92.2 percent spike in the cases of gender-based 
					(GBV) in Kenya.</p>
 
					<p>According to the NCRC, 71 percent of the 2,416 cases of GBV recorded from July were perpetrated on women and girls. That is 
					equivalent to 10 females violated each day during the six-month period.</p>

					<p>The agency identifies males aged between 18 and 33 years as main perpetrators of the vice in its report on Protecting the Family in 
					the Time of Covid-19 Pandemic: Addressing the Escalating Cases of Gender-based Violence, Girl Child Dis-empowerment and Violation of
					Children Rights in Kenya.
					</p>
					
					<p>The report indicates that in the months of April to June there were spikes in 20 different forms of GBV against women, girls, and 
					children. These include defilement, attempted rape, rape, subjecting children to torture, attempted defilement, gang rape, and gang 
					defilement.</p>

					<p>According to the report, other increased GBV offenses were emotional abuse, child marriage, Female Genital Mutilation (FGM), child
					prostitution, child radicalization, and online abuse.</p>
 
					<p>The NCRC also warns that the COVID-19 pandemic has spiked other cases like threatening to kill, attempted murder, arson, sexual 
					communication with a child, sexual abuse, physical abuse, and child abduction and kidnapping were also rife.</p>

					<p>During the COVID-19 pandemic, there have been increased early marriages and rape which have increased abortions, engaging in drug 
					abuse amongst the youth in Kenya.</p>

					<p>The NCRC report also points to depression, trauma, and anxiety as other factors spiked by the pandemics as a result of being 
					neglected, sexually violated by family members and relatives, and confined unlawfully.</p>

					<p>According to the National Crime Research Centre, alcohol, drug, and substance abuse alongside poverty and domestic disputes are 
					some of the major reasons behind the increase in GBV in Kenya during the COVID-19 period.</p>

					<p>The Crimes Ministry also pointed at other factors like retrogressive cultural practices, especially religious beliefs, male 
					dominance, poor parenting, identity crisis among youth, and inadequate support system.</p>

					<p>President Uhuru Kenyatta also raised concerns about the increased tensions within homes. “Cases of gender-based violence have
					increased, mental health issues worsened and instances of teenage pregnancy have also escalated,” Said Uhuru Kenyatta during his 
					State address.</p>

					<p>The NCRC recommends the Ministry of Education to strengthen the curriculum and school syllabus that addresses child rights, 
					reproductive health, and sex education in schools to empower the girl and boy child to speak out against violations of their rights.</P>
