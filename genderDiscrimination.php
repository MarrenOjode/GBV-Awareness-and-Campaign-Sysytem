		<div class="top-sec"><h1> CHALLENGING GENDER DISCRIMINATION: A HOW-TO GUIDE</h1> </div><hr>
          <div class="col-1">
            <div class="section">
			  <img alt="" src="images/gender-discrimination.jpg" style="width:700px; height:400px" /><br><br>
			  
              <p> We are all influenced by gender. Gender norms (or rules) tell us what is appropriate for girls and for boys, women and men to do in our
			  society. Because of gender stereotypes, girls and women are often less valued and have lower social status. </p>
			  
			  <p>Girls and women suffer most of the negative impact of rigid gender norms and roles - they are more likely to experience restrictions of their freedom and mobility, they experience epidemic levels of violence and harassment across the globe and have fewer opportunities to choose how to live their lives. 

				<p>But boys and men suffer too. Ideas about what it means to be a man force boys and men to behave in very limited ways which can harm them.
				Negative masculinities encouraged in boys serve to perpetuate the cycle of discrimination and inequality.</p>

				<p>Denying people the freedom to choose their path in life because of their gender prevents them from fulfilling their full potential. There
				is lots we can do to help build a more gender-equal world. Understanding the deep roots of gender inequality and challenging discrimination
				when we see it are the first steps.</p>
				
				<p><strong>WHAT IS GENDER DISCRIMINATION?</strong><br>
				Gender discrimination describes the situation in which people are treated differently simply because they are male or female, rather than
				on the basis of their individual skills or capabilities. <br>

				The following is a list of discriminatory acts you may come across among your peers, at home, at school or in the workplace. </p>
				 

				<p><strong>Sexual harassment, catcalling</strong><br>

				Harassment and catcalling on the street are prime examples of how women’s right to walk freely around their environment is restricted. 
				The normalisation of harassment and inaction of bystanders and authorities perpetuate this form of discrimination and limit
				women’s freedom.</p>
				
				<p><strong>Gender stereotypes at school and work</strong><br>

				Stereotypes are how societies expect people to act based on their gender. For example, girls should stay at home and help with housework 
				and childcare, should dress modestly and not stay out late at night. People are often judged by how well they adhere to the 
				gender stereotypes. 

				These stereotypes can often bleed out into school and work, where girls are less likely to be encouraged into science and technology
				subjects or leadership roles, due to the perceived ‘male nature’ of these pursuits. Likewise, seemingly positive stereotypes and 
				gender roles such as men being the ‘provider’ or ‘protector’ of the family, put an unnecessary burden on men and boys that could
				more positively be shared in an equal partnership. <br>

				These attitudes limit girls’ power by rendering them less able to help contribute to making the world around them a better place.</p>
				 

				<p><strong>Objectification and poor representation</strong><br>

				Objectification is when a person is treated as a commodity or an object without regard to their personality or dignity. It commonly happens 
				in the media where women are photoshopped and airbrushed leading to much more emphasis and value being put on their external appearance 
				above other capacities. This affects girls’ body image, their self-esteem and ultimately the value they put on themselves.<br> 

				However, girls are also often represented poorly in entertainment and the media in ways that reinforce damaging gender stereotypes and 
				traditional roles. Objectication must be called out and girls must be encouraged to tell their own stories - ones that reflect their power,
				potential and diversity. <br>
				
				<p><strongHOW CAN I CALL OUT GENDER DISCRIMINATION WHEN I SEE IT?</strong><br>
 
				<strong><i>1. Speak out about your own experiences</i></strong><br>

				It’s important, if you feel comfortable, to call out your own experiences of discrimination. This could be highlighting sexist remarks 
				made by your friends or peers or reporting harassment on public transport. <br>

				It’s important to note that there are dangers associated with calling others out on their behaviour. People may not like to have
				their viewpoints challenged; they may react in a negative or aggressive way, so if your instincts tells you that a situation is too risky,
				your own safety is the priority. <br>

				Often the best course of action when discrimination is experienced is to alert an authority figure - a member of staff on public
				transport, or where relevant your teacher or your boss. The more they are made aware these issues are happening, the more they’ll
				be encouraged to step in and be part of the solution. </p>
				 

				<p><strong><i>2. Call out discrimination in the media and advertising</i></strong><br>

				Write to advertisers, marketers and media outlets if you come across stories that portray women and girls in a reductive or unfair way. <br>

				- Are there gender stereotypes in your textbooks at school?<br>
				- Does an advert in your city objectify women?<br>
				- Does a film you’ve watched have fewer speaking roles for women?<br>
				- Find out who is responsible and write to them to let them know your thoughts. <br>

				Also - vote with your feet. Don’t support entertainment that is disrespectful to girls and women and doesn’t represent their true power,
				potential, talent and diversity.</p>

				<p><strong><i>3. Support other campaigners</i></strong><br>

				A great way to make a big effect in the pursuit of gender equality is to join with others. Seek out local campaign groups. Join or set 
				up school or university societies for gender equality and female leadership. There is strength in numbers - more voices tackling a
				particular issue means they are more likely to be heard. <br>

				Helping to raise the profile of campaigns by other marginalised groups is a vital part of ending discrimination. For example, girls 
				from a minority or indigenous community may experience racism as well as sexism, and members of the LGBTIQ+ community may also be 
				experiencing homophobia. Be their allies and lend your voice to their causes. </p>
				 

				<p><strong><i>4. It’s OK to not challenge discrimination every time you see it</i></strong><br>

				As mentioned above, sometimes calling out discrimination can carry risks. It’s not your job alone to fix the world. We are all part 
				of the same movement. <br>

				Just changing how we relate with others, and demanding that others do the same, will not end gender inequality. Lasting change will 
				only happen if the institutions that affect our lives also change. Don’t feel defeated by these big obstacles - each small achievement
				is an extra step towards our shared goal. <br>

				Caring for ourselves means we can continue to advocate for gender equality so always make sure you are maintaining your own wellbeing	</p>



