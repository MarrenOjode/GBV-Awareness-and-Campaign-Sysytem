		<div class="top-sec"><h2 style="color:#000">WOMEN FAITH-BASED GROUPS COLLABORATE TO ERADICATE GBV.</h2> </div><hr>
          <div class="col-1">
            <div class="section">
			  <img alt="" src="images/faith-women.jpg" style="width:700px; height:400px" /><br><br>
			  
              <p> In an effort to eradicate gender based violence in the society, three prominent faith based organizations, Nasrul-Lahi-l-Fatih Society 
			  (NASFAT), Women Wing of Christian Association of Nigeria (WOWICAN) and Federation of Muslim Women Associations in Nigeria (FOMWAN) have
			  come together to train stakeholders on how to investigate, report and refer gender based violence in the community.</p>

			<p>The advocacy program, which is sponsored by The Carter Center, is also running simultaneously in Kano and Enugu. The program had in 
			attendance the traditional rulers of Isefun, Ayobo, Owutu, Ikorodu, and environs. Also in attendance were, Community Service Organisations,
			members of the Nigeria Police Force, representatives of WAPA, religious bodies of both Islam and Christian faith. </p>
			
			<p>The sensitisation program was preceded with the training of men, women, youths and stakeholders on the prevention, roles and 
			responsibilities of personnel on Gender Based Violence (GBV), as well as core principles for effective reporting, referrals and investigation
			with service needs of survivors of GBV.</p>

			<p>NASFAT Focal Point for the project, Alhaja Sadiat Onike-Azeez said that the fight against Gender Based Violence is a religious obligation 
			for everyone. She added that, it is compulsory for everyone to report and act on any case of gender-based violence to end this menace in the
			society.</p>

			<p>“ It is a case of, if you see something, say something and do something” She added that Islam frowns perpetrators of GBV because Shariah is
			all about the promotion of benefits and removal of harm in the society. All hands must be on deck to end GBV,” she said.</p>
			
			<p>NASFAT Coordinator for the United Nation Spotlight Initiative on Ending Violence Against Women and Girls, Alhaja Ganiyat Babalola, 
			enjoined all women to take care of their homes and raise their children with good moral standard, as this will go a long way to end 
			gender-based violence.</p>

