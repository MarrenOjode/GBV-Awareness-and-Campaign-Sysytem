			  <img alt="" src="images/child-day.jpg" style="width:700px; height:400px" /><br><br>
			  
              <p> The Day of the African Child has been celebrated on June 16th every year since 1991 when it was first initiated by the organization of
			  African Unity (OAU).It honors those who participated in the Soweto uprising on 16th June 1991. On June 16th every year governments and all
			  stakeholders involved in child protection gather to discuss and try brainstorm solution to challenges facing the African child.
			  The global theme for 2019 was  “Humanitarian Action in Africa: Children’s Rights First.”</p>

				<p>GVRC hosted this event with the  purpose of providing a platform for the children to showcase their talents and echo out issues they face
				through themed presentations, enlightening them on emerging issues concerning sexual harassment  and also  for us to harvest and document 
				innovation for our  programming.</p>

				<p>Gracing the occasion was Ms. Sylvia the Managing Director of UTK, children from the 20 project schools in Kericho County together with their 
				school heads and Kings and Queens Club Patrons and other guests from Unilever Tea Kenya. There were twenty presentations from all 
				the schools including Kedowa Special School for the Deaf and Dumb, who presented a drama where they were displaying how society overlooks 
				the children with disability and how they are affected by various forms of abuse.  All the presentations from the different schools 
				gyrated around the level of violence, abuse and exploitation of children,child labour, child neglect, defilement and physical assault,
				lack of voice on the issues involving them and negative cultural practices such as FGM, forced marriage and also how to foster healthy
				relationships and respect to human rights amongst boys and girls. The best schools were awarded with certificates and trophies.</p>

				<p>We thank the Unilever team  and the King and Queens Club for the continued support on fighting Child Abuse. </p>

