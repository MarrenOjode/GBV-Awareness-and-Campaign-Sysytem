            <h2 class="title">Sexual and Gender Based Violence</h2>
            <div class="about-content" style="font-size:14px;">
                <div class="column left" >
				
					<div class="text" style="color:brown; font-size:25px;font-weight:900">Gender-Based Violence: An Epidemic</div>
				   <p> Gender-based violence has been described by the World Health Organization as a global public health problem of epidemic proportions and a fundamental 
				   violation of human rights. </p><br>
					
                   <div class="text" style="color:brown; font-size:25px;font-weight:900">What is Sexual and Gender Based Violence?</div>
				   <p> Sexual and gender-based violence (SGBV) refers to any act that is perpetrated against a person’s will and is based on gender 
				   norms and unequal power relationships.</p><br>
				   
				   <p> It includes any form of a non-consensual sexual act, attempt to forcefully obtain a sexual act, unwanted sexual comments or 
				   advances, or acts to traffic women’s sexuality, using coercion, threats of harm or physical force, by any person regardless of 
				   relationship to the survivor, in any setting, including but not limited to home and work. Six core SGBV types include, rape, 
				   sexual assault, physical assault, forced marriage, denial of resources/opportunities or services and psychological and emotional abuse.</p><br>
				   
				   <div class="text" style="color:brown; font-size:25px;font-weight:900">Where does it happen?</div>
				   <p>Girls and young women often experience violence at home, from physical punishment to sexual, emotional or psychological violence. 
				   Acceptance of violence as a ‘private affair’ often prevents others from intervening and prohibits girls and young women from reporting.</p><br>

					<p>School and the journey to it can also be a place where girls experience violence, from sexual harassment, bullying and intimidation. This 
					violation of girls’ rights, especially when committed by those in positions of care or authority, can impact on girls’ ability to continue and 
					complete their education. </p><br>

					<p>In both cities and rural areas, violence against women and girls in public spaces and on public transport is sadly not uncommon. Fear and 
					threats of violence and harassment limit girls’ capacity to lead a free and full life. </p><br>
					
					<p>During emergency situations, girls are also at heightened risk of violence, abuse, exploitation and abuse.</p><br>

					<p>Gender-based violence is also a rising issue in online spaces, with girls and young women reporting harassment and abuse. For many 
					girls, there is pressure to leave online platforms, or self-censor to avoid abuse. This puts the onus on girls to change their behaviour,
					rather than the perpetrators and must be challenged.</p><br>
					
					<div class="text" style="color:brown; font-size:25px;font-weight:900">Why does it happen?</div>
					<p>Gender-based violence occurs in all parts of the world, but the risk is higher where violence is normalised and where rigid concepts 
					of gender exist.<p><br>

					<p>In many cultures, violence towards girls and young women is accepted as a social norm. This must be challenged as a matter of urgency,
					and the blame, shame and stigma faced by victims must be eliminated. </p><br>

					<p>Girls must never be held responsible for the violence that happens to them. Violence is the sole responsibility of the perpetrator,
					who must be held accountable according to national or international legislation. Fear or threat of violence must not restrict girls 
					from living free and full lives, or from realizing their full potential.</p><br>

					<p>Certain groups are more vulnerable to violence, including girls and young women from poor, rural or indigenous communities, those who
					are or are perceived to be LGBTIQ+, those living with disabilities, and girls and women who speak out about political, social and
					cultural issues and gender inequality. </p>
										
					
			</div>
                <div class="column right" style="padding-left:25px">
				<div class="text" style="color:brown; font-size:25px;font-weight:900">How can Gender-based violence be stopped?</div>
					
					<p>Violence is not a private matter – it must be uncovered in order for it to be challenged. Ending gender-based violence will involve 
					action at all levels: challenging social norms that condone violence or impose gender roles; strengthening legislation to criminalise
					violence, and prosecuting the perpetrators.</p><br>

					<p>It’s vital for children to learn about gender-equality at school, just as it's important to promote intergenerational dialogue on 
					violence against children. Community dialogue can challenge the attitudes towards punishment and dominance that perpetuate gender-based
					violence.</p><br>
					 
					<p>We must all promote and strengthen values that support non-violent, respectful, nurturing, positive, gender-equitable relationships 
					for all children and adolescents, including the most vulnerable and excluded. </p><br>
					
				<div class="text" style="color:brown; font-size:25px;font-weight:900">Response Services</div>
					
					<p> SGBV response services include medical treatment (ideally within 72 hours of the incident), psychosocial support, provision 
					of physical security, provision of safe spaces and legal assistance.</p><br>

					<p>Security is provided to SGBV survivors by the national police in collaboration with UNHCR and partners and the survivor following 
					a joint safety assessment; which may include admission into a safe space within the camps or in the community in urban areas for a 
					limited period of time.</p><br>

					<p>Other social services extended to SGBV survivors include community-based peer support, mentorship programmes for girls and boys, 
					provision of dignity and menstrual hygiene management (MHM) kits and livelihood support as a mitigation measure against further violence.
					Note: all service providers must adhere to strict confidentiality.</p><br>
					
                    <div class="text" style="color:brown; font-size:25px;font-weight:900">How to seek help or report concerns.</div>
					<p>You can report SGBV incidents at the nearest hospital or police station, UNHCR. DRC, IRC, TDH, LWF, HI, or any agency in the 
					camps after which you will be advised and/or referred to appropriate and confidential response services. In the urban areas, 
					approach HIAS or NCCK.</p><br>

