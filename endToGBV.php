		<div class="top-sec"><h1> CALL FOR AN END TO GENDER-BASED VIOLENCE</h1> </div><hr>
          <div class="col-1">
            <div class="section">
			  <img alt="" src="images/end-to-gbv.jpg" style="width:700px; height:400px" /><br><br>

					<p>Following the defilement of a 13-year-old girl in Homabay County, women leaders in a press conference held at the Nairobi Women’s 
					Hospital, Adams called for an end to gender-based violence and reiterated the efforts of the Government in apprehending and bringing 
					the perpetrators to task.</p>

					<p>Women leaders arrived at the hospital on Friday, January 11th to offer support to the survivor who was transferred from the HomaBay
					County Referral Hospital. They included Hon. Rachel Shebesh (Chief Administrative Secretary of the Ministry of Public Service, Youth
					and Gender Affairs), Hon. Mishi Mboko (Likoni Member of Parliament) and Hon. Gladys Wanga (Homa Bay County Women’s Representative) 
					who were received by Dr. Sam Thenya (Founder and Director of Strategy, Nairobi Women’s Hospital Group) and Alberta Wambua 
					(Executive Director, GVRC)</p>

					<p>The women leaders, together with GVRC (Gender Violence Recovery Centre), strongly condemned the rise in sexual and gender-based 
					violence that largely affect children. According to 2017-2018 statistics, 41pc of children undergo some form of abuse. Out of this, 
					74pc are sexually abused.</p>

					<p>Hon. Rachel Shebesh stressed that the government ought to put more effort in solving Sexual Gender Based Violence crimes like any 
					other major crime. The police were also urged to avoid delays in the process of evidence collection and conducting investigations. 
					This helps the judicial system prosecute the guilty perpetrators immediately and for health care institutions to provide survivors 
					with services that will assist in a holistic recovery, that is physical, mental and emotional for them to
					reintegrate in their community.</p>

					<p>Hon. CAS revealed that the ministry was in discussions with the Interior CS Fred Matiang’i and IG Boinnet to address the 
					availability of gender desks in every police station. The people manning the desks must be professionals trained in GBV matters. 
					This should increase the response rate to GBV crimes. They also highlighted support of Health Care Assistance Kenya (HAK) which 
					works closely with the police, the County Administration and local health care institutions.</p>

					<p>The great support of Health Care Assistance Kenya (HAK) was highlighted which focuses on raising awareness and advocating against GBV.
					The organization works closely with the police, the County Administration and local health care institutions.</p>

					
