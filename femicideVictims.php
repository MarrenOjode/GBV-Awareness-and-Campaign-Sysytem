					<p> On 30th May, both men and women from different walks of life coloured the University of Nairobi Grounds with white and pink. They 
					expressed the hope that killings of nearly 40 women and other forms of violence across the country in the past few months would end. 
					Families that had lost their loved ones to Femicide were present.</p>

					<p>The event included a series of entertainment pieces – poems, songs and skits – that celebrate women and highlight the challenges 
					they undergo.</p>

					<p style="margin-left:25px">The petition received by Hon.Fred Matiangi and Hon.Margaret Kobia included the following:<br><br>
					
						&#9830; An urge for the President to declare Sexual and Gender Based Violence a national disaster and appoint a special advisor seconded 
						to his office.<br>
						&#9830; Parliament to ensure timely allocation of resources toward prevention, protection and response on SGBV is available to all 
						relevant institutions .A demand that the inter-governmental relation Acts of 2012 to ensure proper coordination services<br>
						&#9830; An urge for the Judiciary to establish special courts ,expedite GBV cases and make offences non bail-able<br>
						&#9830; The construction and resourcing of one-stop centers in all the 47 counties and alternative safe shelter for GBV survivors.<br>
						&#9830; Demand for establishment of a special sex crimes unit within the police force to investigate ,document ,maintain the chain of evidence
						and prosecute cases<br>
						&#9830; Urging bicameral parliament to speed up the implementation of the regulations of the Legal Aid Act ,Witness Protection Act,
						Protection against Domestic Violence 2015,Victim Protection Act, establish the legal aid fund to support free legal aid to survivors 
						of violence<br>
						&#9830; A demand for a National public inquiry for all Femicide cases in Kenya as a way of tracking and seek accountability<br>
						&#9830; The petition was drafted and signed by a number of organizations who were also key in planning and coordination of the event:</p>
