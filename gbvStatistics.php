					<p>&#8226; About 650,000 women and girls in urban informal settlements need access to basic household supplies and dignity kits to reduce
					the risk of GBV.</p>

					<p>&#8226; About 440,000 girls in counties with high prevalence of female genital mutilation (FGM) require social protection and psychosocial 
					support, including dignity kits.<p>

