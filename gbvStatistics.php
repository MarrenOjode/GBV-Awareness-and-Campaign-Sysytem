					<p>&#8226; A total of 530 dignity kits were distributed in Isiolo (100), Nairobi (130), Narok (50), Kajiado (100), Kakamega (100), 
					Meru County (50) and Migori (100) counties.</p>

					<p>&#8226; Field visits we conducted in Kajiado, Kisumu and Kakamega counties to create awareness on SRH and GBV during COVID-19.</p>

